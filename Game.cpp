void InitGame(void)
{
	InitCamera();
	InitPolygon();
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	UninitCamera();
	UninitPolygon();
}

//=====================================================================
// 更新処理
//=====================================================================
