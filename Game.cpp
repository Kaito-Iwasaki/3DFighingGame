{
	UpdatePolygon();
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawGame(void)
{

	GetDevice()->SetRenderState(D3DRS_LIGHTING, FALSE);

	SetCamera();

	DrawPolygon();
}